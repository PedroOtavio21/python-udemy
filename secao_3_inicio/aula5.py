# Tipo de dado bool (Booleano)

"""
- Usado geralmente para casos de verdadeiro ou falso (condicionais, verificações e afins)
- Possui apenas 2 valores: True e False
"""
print("Valores booleanos:")
print(False)
print(True)

# Operadores booleanos

"""
- Há vários exemplos de operadores booleanos em linguagem de programação
- Dentre eles, existe o operador "==", que serve para verificar se um elemento é igual a outro
- Ex.: 5 == '5' (Não)
"""
print("Testes de operadores booleanos:")
print(25 == 25)
print(5 == 2)
print(2 == "Teste")
print(type(5==5)) # Retorna o tipo de dado
