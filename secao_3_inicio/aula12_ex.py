# Exercício: Cálculo de IMC

# Variáveis
nome = 'Luiz Otávio'
altura = 1.80
peso = 95
imc = peso / (altura ** 2)

# Saída de código:
print(nome, 'tem', altura, 'de altura', sep=' ')
print('pesa', peso, 'quilos e seu IMC é', sep=' ')
print(imc)
