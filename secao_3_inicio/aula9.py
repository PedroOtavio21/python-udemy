# Aula 9 de Python

# Operadores Aritiméticos (cada operador falará por si só)

# Soma
# OBS: Utilizar esse operador em strings, realizará uma "concatenação"!
adicao = 10 + 10 
print('Adição:', adicao)

# Subtração
subtracao = 10 - 5
print('Subtração:', subtracao)

# Multiplicação
# OBS: Se você testar este operador com strings e números, ele irá realizar o cálculo! 
multiplicacao = 10 * 10
print('Multiplicação:', multiplicacao)

# Divisão
divisao = 10 / 2.2
print('Divisão', divisao)

# Divisão inteira
# Esta divisão como o nome diz, retorna apenas a parte inteira da divisão
divisao_inteira = 10 // 2.2 
print('Divisão inteira:', divisao_inteira)

# Exponenciação
exponenciacao = 2 ** 10 # 2^10
print('Exponenciação:', exponenciacao)

# Módulo (resto da divisão)
modulo = 55 % 2
print('Módulo:', modulo)