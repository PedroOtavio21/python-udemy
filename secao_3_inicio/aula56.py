"""
Uma tupla é igual a uma lista, porém Imutável

Ou seja, não permite alterações de dados dentro da tupla
"""

"""
Sintaxe de uma tupla:

- Pode não utilizar os colchetes
- Ex.: nomes = 'Pedro', 'João', 'Davi'

- Pode utilizar os dados entre parêntesis
- Ex.: pares = (2, 4, 6, 8)

- Pode utilizar o método de conversão
- Ex.: tupla = tuple([2, 4, 6, 8])
"""

primos = (2, 3, 5, 7, 11)
print(primos[2])