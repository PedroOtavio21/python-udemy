""" Tipos de dados
- String -> str = Dado de Texto
- Estão sempre presentes entre áspas
"""

# Aspas simples
print('Pedro Otávio')

# Aspas duplas
print("João Guilherme") # Não há diferença entre os dois códigos

# Caracteres de escape
print("Luis \\Felipe") # Luis \Felipe

# r
print(r"Luiz \"Otávio\"")

